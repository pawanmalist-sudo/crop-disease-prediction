-- ============================================

-- 1. PREDICTIONS TABLE (Main Table)
CREATE TABLE PREDICTIONS (
    PREDICTION_ID     NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    IMAGE_NAME        VARCHAR2(255)   NOT NULL,
    DISEASE_NAME      VARCHAR2(255)   NOT NULL,
    CONFIDENCE_SCORE  NUMBER(5,2)     NOT NULL,
    STATUS            VARCHAR2(20)    NOT NULL,
    PREDICTED_AT      DATE            DEFAULT SYSDATE
);

-- 2. TOP3_PREDICTIONS TABLE (Chart Data)
CREATE TABLE TOP3_PREDICTIONS (
    TOP3_ID           NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PREDICTION_ID     NUMBER          NOT NULL,
    RANK              NUMBER(1)       NOT NULL,
    DISEASE_NAME      VARCHAR2(255)   NOT NULL,
    CONFIDENCE_SCORE  NUMBER(5,2)     NOT NULL,
    CONSTRAINT FK_PREDICTION FOREIGN KEY (PREDICTION_ID)
        REFERENCES PREDICTIONS(PREDICTION_ID) ON DELETE CASCADE
);

-- 3. CROP_IMAGES TABLE (Image Info)
CREATE TABLE CROP_IMAGES (
    IMAGE_ID          NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
    PREDICTION_ID     NUMBER          NOT NULL,
    IMAGE_NAME        VARCHAR2(255)   NOT NULL,
    IMAGE_PATH        VARCHAR2(500)   NOT NULL,
    UPLOADED_AT       DATE            DEFAULT SYSDATE,
    CONSTRAINT FK_IMAGE_PREDICTION FOREIGN KEY (PREDICTION_ID)
        REFERENCES PREDICTIONS(PREDICTION_ID) ON DELETE CASCADE
);

-- ============================================
-- USEFUL SELECT QUERIES
-- ============================================

-- View all predictions
SELECT * FROM PREDICTIONS ORDER BY PREDICTED_AT DESC;

-- View predictions with top 3
SELECT 
    P.PREDICTION_ID,
    P.IMAGE_NAME,
    P.DISEASE_NAME,
    P.CONFIDENCE_SCORE,
    P.STATUS,
    P.PREDICTED_AT,
    T.RANK,
    T.DISEASE_NAME AS TOP_DISEASE,
    T.CONFIDENCE_SCORE AS TOP_CONFIDENCE
FROM PREDICTIONS P
JOIN TOP3_PREDICTIONS T ON P.PREDICTION_ID = T.PREDICTION_ID
ORDER BY P.PREDICTED_AT DESC, T.RANK;

-- Count total predictions
SELECT COUNT(*) AS TOTAL_PREDICTIONS FROM PREDICTIONS;

-- Count diseased vs healthy
SELECT STATUS, COUNT(*) AS COUNT 
FROM PREDICTIONS 
GROUP BY STATUS;

-- Most common diseases
SELECT DISEASE_NAME, COUNT(*) AS OCCURRENCES
FROM PREDICTIONS
WHERE STATUS = 'Diseased'
GROUP BY DISEASE_NAME
ORDER BY OCCURRENCES DESC;
